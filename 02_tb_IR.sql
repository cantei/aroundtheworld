# HDC
SELECT p.HOSPCODE,p.HN,p.CID
,d.AN,d.DATETIME_ADMIT 
FROM drug_ipd d
LEFT JOIN person p
ON d.HOSPCODE=p.HOSPCODE AND d.PID=p.PID 
WHERE substr(d.DIDSTD,2,10) in ('0021200000','1004010001','1004010002','1004010003','1004010006','1004020001','1004020002','0021500000','1004010004','1004010005')
-- HAVING ISNULL(p.HN)
ORDER BY DATETIME_ADMIT DESC
